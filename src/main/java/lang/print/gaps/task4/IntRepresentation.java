package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c = (int) 120;
        char h = (int) 10;
        char a = (int) 110;
        char r = (int) 130;

        int t = c;
        int u = h;
        int i = a;
        int j = r;

        System.out.println(t);
        System.out.println(u);
        System.out.println(i);
        System.out.println(j);
    }
}
