package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c = '9';
        char h = '7';
        char a = '4';
        char r = '2';

        int t = c - '0';
        int u = h - '0';
        int i = a - '0';
        int j = r - '0';

        System.out.println(t);
        System.out.println(u);
        System.out.println(i);
        System.out.println(j);
    }
}
