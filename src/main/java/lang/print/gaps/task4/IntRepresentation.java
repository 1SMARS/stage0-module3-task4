package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int c = 9;
        int h = 7;
        int a = 4;
        int r = 2;

        char t = (char)(c+'0');
        char u = (char)(h+'0');
        char i = (char)(a+'0');
        char j = (char)(r+'0');

        System.out.println(t);
        System.out.println(u);
        System.out.println(i);
        System.out.println(j);
    }
}
