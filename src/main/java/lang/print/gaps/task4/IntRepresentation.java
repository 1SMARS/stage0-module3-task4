package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int c = 99;
        int h = 79;
        int a = 49;
        int r = 29;

        char t = (char) c;
        char u = (char) h;
        char i = (char) a;
        char j = (char) r;

        System.out.println(t);
        System.out.println(u);
        System.out.println(i);
        System.out.println(j);
    }
}
