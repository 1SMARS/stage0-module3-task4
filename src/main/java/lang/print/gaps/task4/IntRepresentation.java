package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int c =  'c';
        int h =  'h';
        int a =  'a';
        int r =  'r';
        char g = (char)c;
        char q = (char)h;
        char w = (char)a;
        char e = (char)r;


        System.out.println(g);
        System.out.println(q);
        System.out.println(w);
        System.out.println(e);
    }
}
